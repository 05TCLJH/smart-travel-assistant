def _city(aliases: list[str], hotspots: dict[str, list[str]]) -> dict:
    return {"region_type": "city", "aliases": aliases, "hotspots": hotspots}


def _region(
    parent: str,
    anchor: str,
    aliases: list[str],
    hotspots: dict[str, list[str]],
    *,
    radius: float = 100,
    adjacent: list[str] | None = None,
) -> dict:
    payload = {
        "region_type": "scenic_region",
        "parent_province": parent,
        "geocode_anchor": anchor,
        "aliases": aliases,
        "search_radius_km": radius,
        "hotspots": hotspots,
    }
    if adjacent:
        payload["adjacent_admin_tokens"] = adjacent
    return payload


